def pinyin_word(word: str) -> str:
    if word in PINYIN_OVERRIDES:
        return PINYIN_OVERRIDES[word]
    if " " in word:
        return " ".join(pinyin_word(part) for part in word.split())
    return "".join(lazy_pinyin(word, style=Style.TONE, neutral_tone_with_five=False))


def pinyin_sentence(sentence: str) -> str:
    text = " ".join(lazy_pinyin(sentence, style=Style.TONE, neutral_tone_with_five=False))
    replacements = {
        " 。": ".",
        " ，": ",",
        " ？": "?",
        " ！": "!",
        " ：": ":",
        " “ ": " ",
        " ”": "",
        " 、": ",",
    }
    for a, b in replacements.items():
        text = text.replace(a, b)
    return text[:1].upper() + text[1:]


def breakdown(word: str) -> str:
    chunks = []
    for ch in word:
        if ch == " ":
            chunks.append("/")
        elif "\u4e00" <= ch <= "\u9fff":
            chunks.append(f"{ch}({pinyin_word(ch)})")
    return " ".join(chunks)


