def main() -> None:
    lines = INPUT.read_text(encoding="utf-8").splitlines()
    out = []
    changed = 0
    for line in lines:
        if not line or line.startswith("#"):
            out.append(line)
            continue
        fields = line.split("\t")
        word, meaning = fields[1], fields[2]
        replacement = REPLACEMENTS.get((word, meaning)) or REPLACEMENTS.get(word)
        if replacement:
            zh, ko = replacement
            fields[5] = zh
            fields[6] = ko
            fields[7] = sent_pinyin(zh)
            changed += 1
        fields[8] = ""
        fields[9] = ""
        out.append("\t".join(fields))

    OUTPUT.write_text("\n".join(out) + "\n", encoding="utf-8")
    DOWNLOADS_OUTPUT.write_text("\n".join(out) + "\n", encoding="utf-8")
    print(OUTPUT)
    print(DOWNLOADS_OUTPUT)
    print(f"changed={changed}")


if __name__ == "__main__":
    main()
